    <h3>Reactions</h3>

    <h4>Finishing Strike</h4>

    <p>
        When a creature is hit by an attack but not wounded, you may use your quick action to attempt to deal a severe blow against the creature. You make a non-signature attack against the creature. If the creature has more than half of its max <em>HP</em> remaining, the attack automatically misses, and the creature gets a free quick action if it uses it immediately afterward. If the creature has half or fewer of its max <em>HP</em> remaining, however, then any hit automatically reduces the target to 0 <em>HP</em>.
    </p>

