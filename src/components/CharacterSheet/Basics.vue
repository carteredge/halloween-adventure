    <h4>Intercept</h4>
    <p>
        When a creature attempts to move through a space adjacent to you, you can try to block that movement. You roll your <em>Murder</em> against your opponent's <em>Murder</em>, preventing them from moving for the remainder of the turn if you best them.
    </p>


    <h4>Quick Step</h4>
    <p>
        When a creature moves to a space adjacent to you or when a creature in an adjacent square uses the <em>quick step</em> reaction, you may move 5 ft. as a reaction.
    </p>

    <h4>Quick Defense</h4>
    <p>
        When a creature would hit you with an attack, you may use your reaction to defend against that attack.
    </p>

    <h4>Recover</h4>
    <p>
        When you fail a roll for an action, you may use your reaction to reroll the lower die. If the action was a Signature action, you must also use a regular action in place of the Signature.
    </p>

    <h4>Take Cover</h4>
    <p>
        When a creature targets you with a ranged attack or area effect, you may use your reaction to drop prone and move up to 5 ft. in any direction.
    </p>
