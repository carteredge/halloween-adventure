    <h3>Actions</h3>

    <h4>Attack</h4>

    <p>
        Roll one Trait die (typically Murder) and one Skill die. The attack hits if it is greater than or equal to the target's Defense. Roll one Trait die and one weapon damage die.
    </p>

    <h4>Cast Spell</h4>

    <p>
        As an action, choose up to two spell properties. If you're not magical, you can attempt to cast a spell by rolling Spookiness. The difficulty is 5 for casting a spell with one properties or 7 for two properties.
    </p>

    <h4>Dash</h4>

    <p>
        You move up to your movement speed as an action.
    </p>

    <h4>Defend</h4>

    <p>
        Until the start of your next turn you roll <em>Mayhem</em> when attacked, and use that in place of your defense if it is higher. If you have a shield, you can roll <em>Murder</em> instead.
    </p>

    <h4>Drag</h4>

    <p>
        Make a contested <em>Murder</em> roll against a target you have grappled. If you win, you can move yourself and the target up to your speed.
    </p>

    <h4>Grapple</h4>

    <p>
        Make an attack against the target. If it hits, the target is grappled. They may try to escape as an action with contested <em>Murder</em> rolls.
    </p>

    <h4>Hide</h4>

    <p>
        You roll <em>Spookiness</em>. You are hidden to creatures you are obscured from unless they use an action to roll <em>Braaains</em> to contest your <em>Spookiness</em> roll result.
    </p>

    <h4>Push</h4>

    <p>
        Make a contested <em>Murder</em> roll against the target. If you at least roll at or above their defense, you push the target up to 10 ft. away from you. If you also exceed their roll, the target is knocked down. If the target hits an object or fails the contested roll, they are also dealt d4 bludgeoning damage.
    </p>

    <h4>Slam</h4>

    <p>
        Make a contested <em>Murder</em> roll against the target. If you at least roll at or above their defense, you knock the target down. If you also exceed their roll, you deal d6 bludgeoning damage.
    </p>

    <h4>Throw</h4>

    <p>
        Make a contested <em>Murder</em> roll against a grappled target. If you at least roll at or above their defense, you throw the target up to 15 ft. from your location. If you also exceed their roll, the target is knocked down. If the target hits an object or fails the contested roll, they are also dealt d6 bludgeoning damage. 
    </p>

